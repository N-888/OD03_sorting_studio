# Импортируем модуль регулярных выражений, чтобы удобно разделять введенную строку на отдельные числа.
import re


# Создаем пользовательское исключение, чтобы выдавать понятные ошибки при неверном вводе.
class InputDataError(ValueError):
    # Оставляем класс с тем же поведением, что и у ValueError, но с более говорящим именем.
    pass


# Создаем функцию для преобразования текста из поля ввода в список чисел.
def parse_numbers(raw_text: str) -> list[int | float]:
    # Убираем пробелы по краям строки, чтобы пустые символы не мешали проверке.
    cleaned_text = raw_text.strip()

    # Проверяем, что пользователь действительно ввел какие-то данные.
    if not cleaned_text:
        # Вызываем понятную ошибку, если поле оказалось пустым.
        raise InputDataError("Поле ввода пустое. Введите хотя бы одно число.")

    # Делим строку по пробелам, запятым, точкам с запятой и переводам строк.
    tokens = re.split(r"[\s,;]+", cleaned_text)

    # Создаем пустой список, в который будем складывать уже преобразованные числа.
    numbers: list[int | float] = []

    # Проходим по каждому найденному фрагменту текста.
    for token in tokens:
        # Пропускаем случайные пустые элементы, если они вдруг появились после разделения строки.
        if not token:
            # Переходим к следующему элементу без выполнения остального кода ниже.
            continue

        # Проверяем, что текущий фрагмент является целым числом.
        if re.fullmatch(r"[-+]?\d+", token):
            # Преобразуем текст в целое число и добавляем его в список.
            numbers.append(int(token))
        # Проверяем, что текущий фрагмент является вещественным числом.
        elif re.fullmatch(r"[-+]?\d*\.\d+", token):
            # Преобразуем текст в число с плавающей точкой и добавляем его в список.
            numbers.append(float(token))
        # Обрабатываем ситуацию, когда введено что-то, что не похоже на число.
        else:
            # Вызываем ошибку с подсказкой, какой именно фрагмент ввода оказался неверным.
            raise InputDataError(f"Не удалось распознать число: {token}")

    # Проверяем, что после разбора списка у нас действительно остались числа.
    if not numbers:
        # Вызываем ошибку, если список почему-то оказался пустым.
        raise InputDataError("После обработки строки не найдено ни одного числа.")

    # Возвращаем готовый список чисел обратно в программу.
    return numbers


# Создаем функцию для красивого преобразования списка чисел в строку.
def format_numbers(numbers: list[int | float]) -> str:
    # Собираем все элементы в одну строку, чтобы вывод в интерфейсе был аккуратным и читаемым.
    return ", ".join(format_number(number) for number in numbers)


# Создаем функцию для аккуратного отображения одного числа без лишнего .0 у целых значений.
def format_number(number: int | float) -> str:
    # Проверяем, что число имеет тип float и при этом не содержит дробной части.
    if isinstance(number, float) and number.is_integer():
        # Преобразуем такое число в целое, чтобы показать его без десятичной точки.
        return str(int(number))

    # Возвращаем обычное строковое представление числа во всех остальных случаях.
    return str(number)