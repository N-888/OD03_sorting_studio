# Импортируем библиотеку customtkinter, чтобы настроить внешний вид приложения до его запуска.
import customtkinter as ctk

# Импортируем главный класс окна из нашего модуля интерфейса.
from app.ui import SortingStudioApp


# Проверяем, что файл запущен напрямую, а не импортирован как модуль.
if __name__ == "__main__":
    # Включаем автоматический выбор светлой или темной темы по настройкам системы.
    ctk.set_appearance_mode("System")
    # Выбираем стандартную цветовую тему интерфейса.
    ctk.set_default_color_theme("blue")
    # Создаем экземпляр нашего главного окна.
    app = SortingStudioApp()
    # Запускаем бесконечный цикл обработки событий окна.
    app.mainloop()